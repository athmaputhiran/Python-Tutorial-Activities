#Get the 2 numbers from the user

first_num = int(input("Enter the first number:" ))
sec_num = int(input("Enter the second number: "))

# Perform arithmetic operation
addition = first_num + sec_num
subtraction = first_num - sec_num
multiplication = first_num * sec_num
division = first_num / sec_num

#Print result
print(f"The sum is {addition}")
print(f"The subraction of 2 numbers  is {subtraction}")
print(f"The subtraction of 2 numbers is {multiplication}")
print(f"The division of 2 numbers is {division}")


